package week1.day2;

public class OddorEven {

	public static void main(String[] args) {
				
				for (int a=1;a<=10;a++)
						{
					    if (a%2==0)
					    {
					    	
					        System.out.println("The number is EVEN:"  +a);
						}
				
					    else
					    {
				    System.out.println("The number is ODD:"  +a);
						
			}

	}

}
}